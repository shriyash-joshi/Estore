<h3>CBSE: The Central Board of Secondary Education</h3>

<p>
    CBSE, also known as the Central Board of Secondary Education, was established in 1962, making it one of the oldest education boards in the country. CBSE is
    known for its focus on Mathematics and Sciences and is suitable for students interested in Engineering/Medicine.
</p>
<p>A CBSE education:</p>
<ul>
    <li>Prepares for competitive examinations: CBSE is the basis for exams like the JEE and NEET taken by lakhs of Engineering and Medicine aspirants
        respectively. In addition, the UPSC examinations for Civil services are also based on NCERT text books which are the mainstay of CBSE curriculum.
    </li>
    <li>Is popular: CBSE schools have the widest network across India, making the curriculum popular among families that need to relocate between cities
        frequently.
    </li>
    <li>Is academically flexible: CBSE offers course choices which enable students to broaden their knowledge base. For example, a Science student can opt
        for Economics without actually taking up additional subjects in Commerce.
    </li>
</ul>