<h3>CAMBRIDGE: Cambridge International</h3>
<p>
    The Cambridge Assessment International Education, popularly known as “Cambridge International”, also offers a curriculum for class 11 and 12 apart from
    IGCSE and O-Level (which is offered till class 10). This curriculum is called the Cambridge International A-Level Qualification.
</p>
<p>
    A Cambridge International education:
</p>
<ul>
    <li>Promotes holistic learning and all-round development.</li>
    <li>Is flexible with respect to subject choices: The affiliated schools offer a large number of subject choices. This offers more flexibility to the
        students and helps them select the subjects based on choice.
    </li>
    <li>Focuses on extracurricular activities: The curriculum focuses on holistic development which means that extracurricular activities are embedded in the
        curriculum itself. The syllabus has made it mandatory to be involved and participate in extracurricular activities.
    </li>
</ul>
