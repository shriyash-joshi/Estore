<h3 class="mb-0">CISCE: Indian Certificate of Secondary Education</h3>

<p>
    Established in 1958, the CISCE, also known as The Council for the Indian School Certificate Examinations is a national level private education board. The
    CISCE conducts examinations for Grade 10, called the Indian Certificate of Secondary Education (ICSE) and also for Grade 12, called the Indian School
    Certificate (ISC).
</p>
<p>A CISCE education:</p>
<ul>
    <li>Is globally recognized: The CISCE curricula has been nationally and internationally recognized by several foreign institutions.</li>
    <li>Has a balanced syllabus: The CISCE curriculum is known to have a broader focus on Maths, Science as well as offering an equal preference to Arts,
        Languages and Humanities.
    </li>
    <li>Provides a comprehensive foundation: The CISCE curriculum offers an experiential approach to learning thus broadening understanding. In addition, a
        heavy weightage is given to project work, research, group assignments and internal assessments.
    </li>
</ul>.